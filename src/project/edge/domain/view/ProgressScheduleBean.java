package project.edge.domain.view;

import garage.origin.domain.view.ViewBean;

public class ProgressScheduleBean implements ViewBean {

    private String id;
    
    private String seqNo;
    private String scheduleName;
    private String version;
    private String isRun;
    private String processState;
    private String mainSchedule;
    private String scheduleEditor;
    private String EditDate;
    
    @Override
    public String getId() {
        return this.id;
    }

    
    public String getSeqNo() {
        return this.seqNo;
    }

    
    public void setSeqNo(String seqNo) {
        this.seqNo = seqNo;
    }

    
    public String getScheduleName() {
        return this.scheduleName;
    }

    
    public void setScheduleName(String scheduleName) {
        this.scheduleName = scheduleName;
    }

    
    public String getVersion() {
        return this.version;
    }

    
    public void setVersion(String version) {
        this.version = version;
    }

    
    public String getIsRun() {
        return this.isRun;
    }

    
    public void setIsRun(String isRun) {
        this.isRun = isRun;
    }

    
    public String getProcessState() {
        return this.processState;
    }

    
    public void setProcessState(String processState) {
        this.processState = processState;
    }

    
    public String getMainSchedule() {
        return this.mainSchedule;
    }

    
    public void setMainSchedule(String mainSchedule) {
        this.mainSchedule = mainSchedule;
    }

    
    public String getScheduleEditor() {
        return this.scheduleEditor;
    }

    
    public void setScheduleEditor(String scheduleEditor) {
        this.scheduleEditor = scheduleEditor;
    }

    
    public String getEditDate() {
        return this.EditDate;
    }

    
    public void setEditDate(String editDate) {
        this.EditDate = editDate;
    }

    
    public void setId(String id) {
        this.id = id;
    }

}
