package project.edge.domain.view;

import garage.origin.domain.view.ViewBean;

/**
 * @author angel_000
 *         保存组织信息的表现层DTO。
 */
public class OrgBean implements ViewBean {

    private String id;

    private String orgName;
    private String shortName;
    private String orgCode;
    private String pid;
    private int level;
    private int orgOrder;
    
    private String syncStatusText;

    @Override
    public String getId() {
        return this.id;
    }


    public String getOrgName() {
        return this.orgName;
    }


    public void setOrgName(String orgName) {
        this.orgName = orgName;
    }


    public String getShortName() {
        return this.shortName;
    }


    public void setShortName(String shortName) {
        this.shortName = shortName;
    }


    public String getOrgCode() {
        return this.orgCode;
    }


    public void setOrgCode(String orgCode) {
        this.orgCode = orgCode;
    }


    public String getPid() {
        return this.pid;
    }


    public void setPid(String pid) {
        this.pid = pid;
    }


    public int getLevel() {
        return this.level;
    }


    public void setLevel(int level) {
        this.level = level;
    }


    public int getOrgOrder() {
        return this.orgOrder;
    }


    public void setOrgOrder(int orgOrder) {
        this.orgOrder = orgOrder;
    }


    public void setId(String id) {
        this.id = id;
    }

    public String getSyncStatusText() {
        return this.syncStatusText;
    }

    public void setSyncStatusText(String syncStatusText) {
        this.syncStatusText = syncStatusText;
    }

}
