package project.edge.domain.view;

import java.util.ArrayList;
import java.util.List;

import garage.origin.domain.view.ViewBean;

/**
 * 流程表的表现层DTO。
 */
public class FlowableSettingBean implements ViewBean {

    private String id;
    private String flowName;

    private short isComplete;
    private String isCompleteText;

    private short isDisable;
    private String isDisableText;

    private String page_;
    private String pageText;
    private String remark;

    private List<String> projectRoleIdList = new ArrayList<String>();
    private List<FlowableSettingNodeBean> flowableSettingNodeBeans = new ArrayList<>();

    public String getFlowName() {
        return this.flowName;
    }

    public void setFlowName(String flowName) {
        this.flowName = flowName;
    }

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public short getIsComplete() {
        return this.isComplete;
    }

    public void setIsComplete(short isComplete) {
        this.isComplete = isComplete;
    }

    public String getIsCompleteText() {
        return this.isCompleteText;
    }

    public void setIsCompleteText(String isCompleteText) {
        this.isCompleteText = isCompleteText;
    }

    public short getIsDisable() {
        return this.isDisable;
    }

    public void setIsDisable(short isDisable) {
        this.isDisable = isDisable;
    }

    public String getIsDisableText() {
        return this.isDisableText;
    }

    public void setIsDisableText(String isDisableText) {
        this.isDisableText = isDisableText;
    }

    public String getPage_() {
        return this.page_;
    }

    public void setPage_(String page_) {
        this.page_ = page_;
    }

    public String getPageText() {
        return this.pageText;
    }

    public void setPageText(String pageText) {
        this.pageText = pageText;
    }

    public String getRemark() {
        return this.remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public List<String> getProjectRoleIdList() {
        return this.projectRoleIdList;
    }

    public void setProjectRoleIdList(List<String> projectRoleIdList) {
        this.projectRoleIdList = projectRoleIdList;
    }

    public List<FlowableSettingNodeBean> getFlowableSettingNodeBeans() {
        return this.flowableSettingNodeBeans;
    }

    public void setFlowableSettingNodeBeans(
            List<FlowableSettingNodeBean> flowableSettingNodeBeans) {
        this.flowableSettingNodeBeans = flowableSettingNodeBeans;
    }

}
