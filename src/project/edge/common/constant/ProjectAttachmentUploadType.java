package project.edge.common.constant;

/**
 * 项目的文件上传类型，用来区分是项目建议文件上传画面、项目可研文件上传画面还是项目初设文件画面提交的文件。
 * 
 * 普通修改画面和新建画面的uploadType都是空字符串。
 *
 */
public interface ProjectAttachmentUploadType {

    /**
     * 项目建议文件上传画面的文件
     */
    String PROPOSAL = "proposal";

    /**
     * 项目可研文件上传画面的文件
     */
    String FEASIBILITY = "feasibility";

    /**
     * 项目初设文件画面的文件
     */
    String PRELIMINARY_DESIGN = "preliminaryDesign";
}
